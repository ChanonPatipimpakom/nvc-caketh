package model;

public class TeamResult {
	private String name;
	private int totalPound;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTotalPound() {
		return totalPound;
	}
	public void setTotalPound(int totalPound) {
		this.totalPound = totalPound;
	}
	
	
}
